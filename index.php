        // Enhanced intersection observer for animations
        document.addEventListener('DOMContentLoaded', function () {
            // Enhanced fade-in animations
            const fadeElements = document.querySelectorAll('.fade-in, .fade-in-left, .fade-in-right');

            const fadeInOptions = {
                threshold: 0.2,
                rootMargin: '0px 0px -50px 0px'
            };

            const fadeInObserver = new IntersectionObserver(function (entries, observer) {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        if (entry.target.classList.contains('fade-in')) {
                            entry.target.classList.add('visible');
                        } else if (entry.target.classList.contains('fade-in-left')) {
                            entry.target.classList.add('visible');
                        } else if (entry.target.classList.contains('fade-in-right')) {
                            entry.target.classList.add('visible');
                        }
                        observer.unobserve(entry.target);
                    }
                });
            }, fadeInOptions);

            fadeElements.forEach(element => {
                fadeInObserver.observe(element);
            });

            // Enhanced statistics counter with easing
            const statElements = document.querySelectorAll('.stat-number');
            const statsOptions = {
                threshold: 0.3
            };

            const statsObserver = new IntersectionObserver(function (entries, observer) {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const element = entry.target;
                        const target = parseInt(element.getAttribute('data-count'));
                        const duration = 2500;
                        const startTime = performance.now();

                        function easeOutExpo(t) {
                            return t === 1 ? 1 : 1 - Math.pow(2, -10 * t);
                        }

                        function updateCounter(currentTime) {
                            const elapsed = currentTime - startTime;
                            const progress = Math.min(elapsed / duration, 1);
                            const easedProgress = easeOutExpo(progress);

                            const currentValue = Math.round(target * easedProgress);
                            element.textContent = currentValue;

                            if (progress < 1) {
                                requestAnimationFrame(updateCounter);
                            } else {
                                element.textContent = target;
                            }
                        }

                        requestAnimationFrame(updateCounter);
                        observer.unobserve(entry.target);
                    }
                });
            }, statsOptions);

            statElements.forEach(element => {
                statsObserver.observe(element);
            });

            // Enhanced smooth scrolling
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });

            // Enhanced button interactions
            document.querySelectorAll('.btn-primary, .btn-secondary').forEach(button => {
